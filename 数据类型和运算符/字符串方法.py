# .title 返回每个首字母大写
print('hello ppx'.title()) # Hello Ppx

# islower 判断这个字符串是否都是 小写
full_name = 'hahah ppx'
print(full_name.islower()) # True
print('Hhaha ppx'.islower()) # False

# count  返回传入参数出现次数
print('haha haha ppx'.count('haha')) # 2

# capitalize 第一个字符转换为大写，其余字符转换为小写
print('hahha ooop'.capitalize()) # Hahha ooop

# lower 所有字符转换为小写
print('AKAKAKAKKA'.lower()) # akakakakka

# upper 所有字符转换为大写
print('akakakakka'.upper()) # AKAKAKAKKA

# find 找指定位置
print('word hey'.find('hey')) # 5